package Processamento_Atribuição;
public class Operadores_Aritimeticos {
     public static void main(String[] args) {
       int y = 6;
       int x = y++;
       int a = x + y;
       System.out.println("x = " + x);
       System.out.println("y = " + y);
       System.out.println("x + y = "+ a); 
    
       double d = 2.5;
       d += 2; // d = d + 2 
       System.out.println(d);
    }


   
    
}
